//internal conversion of Character to integer
public class Main
{
	public static void main(String[] args) {
		int a=65;
		
		switch(a)
		{
		   case 66: System.out.println("10");
		           break;
		  case 'A':System.out.println("20");
		          break;
		   case 30: System.out.println("30");
		        break;
		  default:
		    System.out.println("default");
		}
	}
}	
